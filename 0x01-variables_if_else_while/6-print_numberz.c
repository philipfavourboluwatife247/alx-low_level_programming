#include <stdio.h>
#include <stdlib.h>
/**
 * main - Write a program that prints all single digit numbers of base 10 starting from 0, followed by a new line
 *
 * Return: return types is 0
 */
int main(void)
{
	int p;

	p = 0;
	while
		(p < 10) {
			putchar(p + '0');
			p++;
		}
		putchar('\n');
		return (0);

}

